package com.xinwu.gof23.structure.flyweight;

//抽象享元类
public interface ChessFlyWeight {
    String getColor();
    void display(Coordinate coordinate);
}

class Coordinate {
    private int x, y;

    public Coordinate(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}